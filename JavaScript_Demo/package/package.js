//包管理
//包: 可复用代码，包括库，工具，共享组件，个人应用，或称依赖项和模块
//NPM: 包管理器，使用package.json文件，包括以下几项
//     metedata: 项目名称，版本，描述，作者，仓库，证书
//     依赖项: 用到的包
//     脚本
//npm init: 初始化项目
//npm install <package-name>: 下载需要的包
//npm install --save-dev <package-name>: 在dev依赖项中添加包，即在文件不同地方添加包
//      dependencies: 生产中运行
//      devdependenices: 开发中协助的包，比如库，编译工具放这里，只在开发中使用，不在生产中使用
//安装和使用:
//      npm init: 创建package.json文件
//      npm install: 下载包, 比如express
//      npm run start/npm start: 启动express服务，或者run 脚本中的命令
